        $method = $request->input('add_method');
        
        if($method === 'from_image') {
            $file = $request->file('book_image');
            $info = BookServices::getBookInfo($file);
        } 

        if($method ==='from_isbn') {
            $ISBNNumber = $request->input('isbn');
            $info = BookServices::getBookDetails($ISBNNumber);
        }
    }

